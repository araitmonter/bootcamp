//Operadores relacionales o de comparacion: para hacer comparaciones entre variables

// Mayor que: a > b "a es mayor que b"
// Menor que: a < b "a es menor que b"
// Mayor o igual que: a >= b "a es mayor o igual que b"
// Menor o igual que: a <= b "a es menor o igual que b"
// Igual: a == b "a es igual a b"
// Distinto: a != b "a es diferente de b"
// Operador estricto: ===
// Operador diferente estricto: !==

let numero1 = 22;
let numero2 = 15;


console.log("#1: " + numero1);
console.log("#2: " + numero2);

console.log("El #1 es mayor al #2: " + (numero1 > numero2));
console.log("El #1 es menor al #2: " + (numero1 < numero2));
console.log("El #1 es mayor o igual al #2: " + (numero1 >= numero2));
console.log("El #1 es menor o igual al #2: " + (numero1 <= numero2));
console.log("El #1 es igual al #2: " + (numero1 == numero2));
console.log("El #1 es diferente al #2: " + (numero1 != numero2));
