function sumar (numero1, numero2) { //entre parentesis van los parametros
    let suma = numero1 + numero2;
    document.write('<h1>' + suma + '<h1>');
}

sumar(3,2); //para llamar la función, los parametros van adentro de los parentesis
sumar(5,1);
sumar(38, 52);

